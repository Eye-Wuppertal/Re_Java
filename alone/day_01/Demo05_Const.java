public class Demo05_Const{
    public static void main(String[] args){
        int a = 10;
        int b = 10;
        System.out.println("-----a++-----");
        System.out.println(a++);//10
        System.out.println(a);//11
        System.out.println("-----++b-----");
        System.out.println(++b);//11
        System.out.println(b);//11
    }
}